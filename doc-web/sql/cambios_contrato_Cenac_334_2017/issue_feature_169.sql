-- -----------------------------------------------------------------------------
-- TRIGGER: TRG_REGISTRO_H_NOTIFICACION
-- -----------------------------------------------------------------------------

CREATE OR REPLACE TRIGGER TRG_REGISTRO_H_NOTIFICACION
AFTER INSERT OR UPDATE ON NOTIFICACION 
FOR EACH ROW
BEGIN
    INSERT INTO H_NOTIFICACION
    (
        HNT_ID,      
        TNF_ID,
        CLA_ID,
        ASUNTO,
        CUERPO,    
        ACTIVO,    
        QUIEN,     
        CUANDO,    
        QUIEN_MOD, 
        CUANDO_MOD
    ) VALUES (
        H_NOTIFICACION_SEQ.NEXTVAL,      
        :NEW.TNF_ID,
        :NEW.CLA_ID,
        :NEW.ASUNTO,
        :NEW.CUERPO,    
        :NEW.ACTIVO,    
        :NEW.QUIEN,     
        :NEW.CUANDO,    
        :NEW.QUIEN_MOD, 
        :NEW.CUANDO_MOD
    );
END;


