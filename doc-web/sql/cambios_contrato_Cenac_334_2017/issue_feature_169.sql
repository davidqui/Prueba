    NTF_ID              NUMBER(38)          NOT NULL,
    TNF_ID              NUMBER(38)          NOT NULL,
    CLA_ID              NUMBER(38)          NOT NULL,
    ASUNTO              VARCHAR2(64 CHAR)   NOT NULL,
